public class DeclaracaoVariavel {
    public static void main(String[] args) {
        System.out.println("Olá, mundo!"); 

        // Declaração de variáveis

        String meuNome = "João";
        int minhaIdade = 20;
        double meuPeso = 70.5;
        boolean souEstudante = true;

        System.out.println("Meu nome é " + meuNome);
        System.out.println("Tenho " + minhaIdade + " anos");
        System.out.println("Peso " + meuPeso + " kg");
        
        // Condicional if-else
        if (souEstudante) {
            System.out.println("Sou estudante");
        } else {
            System.out.println("Não sou estudante");
        }
    }
}
